        {
            //if you've configured it at /blog or /whatever, set that pathbase so ~ will generate correctly
            //Uri rootUri = new Uri(Environment.GetEnvironmentVariable("ASPNETCORE_APPL_PATH"));
            //string path = rootUri.AbsolutePath;

            ////Deal with path base and proxies that change the request path
            //if (path != "/")
            //{
            //    app.Use((context, next) =>
            //    {
            //        if (context.Request.Path.StartsWithSegments(path, out var remaining))
            //        {
            //            context.Request.PathBase = path;
            //            context.Request.Path = remaining;
            //        }
            //        else
            //        {
            //            context.Request.PathBase = new PathString(path);
            //        }
            //        return next.Invoke();
            //    });
            //}

            var forwardOptions = new ForwardedHeadersOptions
            {
                ForwardedHeaders = ForwardedHeaders.XForwardedFor | ForwardedHeaders.XForwardedProto,
                RequireHeaderSymmetry = false
            };

            forwardOptions.KnownNetworks.Clear();
            forwardOptions.KnownProxies.Clear();
            
            app.UseForwardedHeaders(forwardOptions);

            app.UseCors("CorsPolicy");
            
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
                app.UseDatabaseErrorPage();
            }
            else
            {
                app.UseExceptionHandler("/Home/Error");
            }

            app.UseDefaultFiles();
            app.UseStaticFiles();

            app.UseRouting();
            app.UseIdentityServer();
            app.UseAuthorization();

            app.UseAdminUI();

            app.UseEndpoints(endpoints => endpoints.MapDefaultControllerRoute());
